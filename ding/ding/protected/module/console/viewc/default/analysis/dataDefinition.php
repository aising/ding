	<h4 id="gold">2.	金币数据</h4>
	<p>金币总量：所有累计用户身上的金币总和，无论该用户是否登录活跃，只要身上有金币，全部累加，取每天0点时的玩家金币数量累加</p>
	<p>金币发放：游戏内所有可以产出金币渠道汇总，包括</p>
	<p>1.	注册送：新手玩家注册时会赠送一定数量的金币，当前配置的是1万金币</p>
	<p>2.	破产送：玩家金币低于100时，系统会对玩家发放2000金币救济，每天2次机会</p>
	<p>3.	购买送：玩家购买游戏币赠送的金币，包括达到累计充值额度时赠送给玩家的金币数</p>
	<p>4.	活动送：在活动上获得的金币。特别的：当前A计划的金币大多数采用任务的形式添加活动，不算在活动发放内，而算在任务发放内。如果是登录类活动、页面上的活动获得的金币统计到活动送里面</p>
	<p>5.	比赛送：比赛中获得名次，赢得金币</p>
	<p>6.	任务送：房间内任务完成后发放的金币数</p>
	<p>7.	后台发放：通过经分后台、客服后台给玩家补发与扣除的金币总和，该值可以为负值</p>
	<p>8.	连续登录送：每日连续登录奖励发放金币总和</p>
	<p>9.	每日抽奖机送：每天抽奖机发放金币总和</p>
	<p>金币回收：游戏内所有可以消耗金币渠道汇总，包括</p>
	<p>1.	比赛报名费扣除：每场比赛收取的报名费数量之和</p>
	<p>2.	娱乐场玩牌台费消耗：娱乐场每玩1局都会对玩牌玩家收取一定的金币</p>
	<p>3.	送小费：牌桌上送给荷官（系统）的金币数</p>
	<p>4.	买荷官：牌桌上换荷官花费的金币数</p>
	<p>其他：不在上述发放和消耗途径中的金币造成的发放与消耗的和值，可以为负值</p>
	<p>平衡值的意义在于验证金币系统的稳定性
（昨日今日总量-前天金币总量）的值理论上应该与（昨日金币发放-昨日金币回收）的值相等，实际上肯定会有一些差异，差值越大说明我们的统计越不准
</p>
