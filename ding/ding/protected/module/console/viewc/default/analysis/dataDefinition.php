	<h4 id="report">1.	运营报表</h4>
	<h5>登录数据部分</h5>
	<p>日登录账号数：当天登录的所有账号数量</p>
	<p>日登陆用户数：当天登陆的所有设备数量</p>
	<p>日注册账号数：当天注册的账号数</p>
	<p>日注册用户数：当天注册的设备数量</p>
	<p>日收入：当天所有登陆用户带来的收入总和，单位：元</p>
	<p>账号1局转化率：当天玩牌局数≥1局账号数/日登录账号数</p>
	<p>用户1局转化率：当天所有账号玩牌局数总和≥1局的设备数/日登录用户数</p>
	<p>账号5局转化率：当天玩牌局数≥5局账号数/日登录账号数</p>
	<p>用户5局转化率：当天所有账号玩牌局数总和≥5局的设备数/日登录用户数</p>
	<p>账号平均游戏时长：日登录玩家的累计在房间内玩牌时长总和/日登录账号数</p>
	<p>用户平均游戏时长：日登录玩家的累计在房间内玩牌时长总和/日登录用户数</p>
	<p>最高同时在线人数：当天最高同时在线最高人数的数量</p>
	<p>最高同时在线时间点：出现最高同时在线人数的时间点，精确到分钟即可</p>
	<p>日付费账号数：当天所有付费的账号数数量总和</p>
	<p>付费用户数：当天所有付费的设备数数量总和</p>
	<p>付费渗透率（账号）：日付费账号数/日登录账号数</p>
	<p>付费渗透率（用户）：日付费用户数/日登录用户数</p>
	<p>Arppu值（账号）：日收入/日付费账号数</p>
	<p>Arppu值（用户）：日收入/日付费用户数</p>
